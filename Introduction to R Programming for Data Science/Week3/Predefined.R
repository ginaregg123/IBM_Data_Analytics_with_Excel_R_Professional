ratings <- c(8.7, 6.9, 8.5)
mean(ratings)

#  use the sort() function to sort the movies rating in ascending order.
sort(ratings)

# sort by decreasing order, by adding in the argument decreasing = TRUE.
sort(ratings, decreasing = TRUE)

